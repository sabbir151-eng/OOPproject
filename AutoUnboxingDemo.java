package myproject;
import java.util.*;


public class AutoUnboxingDemo {
	 public static void main(String[] args) {
	        Integer a = 100;
	        int x = a;

	        Double b = 45.67;
	        double y = b;

	        Character c = 'B';
	        char z = c;

	        System.out.println(x);
	        System.out.println(y);
	        System.out.println(z);
	    }

}
