package myproject;
import java.util.Scanner;
public class BitwiseOperators {
	public static void main(String[] args) {
        int a = 5;
        int b = 3;

        System.out.println("a & b = " + (a & b));
        System.out.println("a | b = " + (a | b));
        System.out.println("a ^ b = " + (a ^ b));
        System.out.println("~a = " + (~a));
        System.out.println("~b = " + (~b));
    }
}



