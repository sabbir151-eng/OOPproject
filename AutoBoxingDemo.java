package myproject;
import java.util.*;

public class AutoBoxingDemo {
	public static void main(String[] args) {
        int a = 50;
        Integer x = a;

        char b = 'A';
        Character y = b;

        double c = 12.34;
        Double z = c;

        System.out.println(x);
        System.out.println(y);
        System.out.println(z);
    }

}
