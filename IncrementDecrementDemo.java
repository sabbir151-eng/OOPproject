package myproject;
import java.util.Scanner;
public class IncrementDecrementDemo {
	public static void main(String[] args) {
        int a = 5;
        a++;
        System.out.println("After a++: " + a);
        ++a;
        System.out.println("After ++a: " + a);
        a--;
        System.out.println("After a--: " + a);
        --a;
        System.out.println("After --a: " + a);
    }

}
